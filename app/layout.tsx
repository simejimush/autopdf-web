import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { createSupabaseServerClient } from "@/lib/supabase/server";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const supabase = await createSupabaseServerClient();

  const {
    data: { user },
  } = await supabase.auth.getUser();

  let isGoogleConnected = false;

  if (user) {
    const { data } = await supabase
      .from("google_connections")
      .select("id")
      .eq("user_id", user.id)
      .maybeSingle();

    isGoogleConnected = !!data;
  }
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {user && !isGoogleConnected && (
          <div className="ap-banner">
            <span className="ap-banner__text">
              Googleアカウントが未接続です。自動実行を利用するには接続が必要です。
            </span>

            <a href="/api/google/connect" className="ap-banner__action">
              <button className="ap-banner__btn">Googleに接続</button>
            </a>
          </div>
        )}

        {children}
        <style>{`
  .ap-banner{
  background:#FEF3C7;
  border-bottom:1px solid #F59E0B;
  padding:12px 20px;
  display:flex;
  justify-content:space-between;
  align-items:center;
  gap:12px;
  font-size:14px;
  color:#1F2937; /* ← 追加してもOK */
}
  .ap-banner__text{
  line-height:1.4;
  color:#1F2937; /* ← 追加（濃いグレー） */
  font-weight:500;
}
  .ap-banner__action{
    flex:0 0 auto;
  }
  .ap-banner__btn{
    background:#4F46E5;
    color:#fff;
    padding:8px 14px;
    border-radius:999px;
    border:none;
    cursor:pointer;
    font-weight:600;
    white-space:nowrap;
  }

  /* ✅ スマホだけ：テキストの下にボタン、ボタンは右寄せ（3枚目の配置） */
  @media (max-width: 640px){
    .ap-banner{
      flex-direction:column;
      align-items:stretch;
    }
    .ap-banner__action{
      display:flex;
      justify-content:flex-end;
    }
  }
`}</style>
      </body>
    </html>
  );
}
